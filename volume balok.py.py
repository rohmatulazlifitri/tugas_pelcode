#program menghitung volume balok

print("program menghitung volume balok")

p = int(input("masukan panjang balok:"))
l = int(input("masukan lebar balok:"))
t = int(input("masukan tinggi balok"))

v = p * l * t
print("rumus menghitung volume balok = p x l x t")
print("hasil dari hitung volume balok:",v)
